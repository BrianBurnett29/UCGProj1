if student_grade >= 90:
    print(f"Your grade is {student_grade}")
    print("Excellent: You got an A!\n")
    print("=" * 40)
elif student_grade >= 80:
    print(f"Your grade is {student_grade}")
    print("Great job! You got a B\n")
    print("=" * 40)
else:
    print(f"Your grade is {student_grade}")
    print("Keep studying! You can do better!\n")
    print("=" * 40)
 

